	<h2 class="orange-text" id="browser">操作方法</h2>
	<h3>やっちゃダメなこと</h3>
	<p>
		画面上部の「Flip」をクリック→ちゃぶ台返しです。<br>
		CREATEした本人は Option > Permissions から、禁止することができますが、<br>
		CREATEした本人の操作は禁止されないので気を付けてください。
	</p>
	<h3>ゲーム開始</h3>
	<p>
		<ul>
			<li>だれか1人がCREATE (Server NameとPasswordを設定して、みんなに共有）</li>
			<li>残りのメンバーはJOIN</li>
		</ul>
	</p>
	<h3>場決め</h3>
	<p>
		<ul>
			<li>トランプがあるので、それで場決めします。Aがスタートプレイヤー（白）</li>
			<li>場所が決まったら、右上のプレイヤー名をクリックしてChange Colorを選択→位置に合わせて座る</li>
		</ul>
	</p>
	<h3>ドラフト</h3>
	<p>
		<ul>
			<li>START GAMEはまだクリックしない（クリックするとラウンド1がめくられてしまう</li>
			<li>デッキの上で左クリック長押しをすると、デッキ全体をつかむことができる</li>
			<li>デッキを重ねると、デッキがまとまる。まとまらない場合は、D&Dで選択してから「G」でまとまる</li>
			<li>デッキを右クリック＞Shuffle でシャッフルする（ドラッグしたまま、マウスを振ってもシャッフルできる）</li>
			<li>デッキを右クリック＞Cut に合わせると右にゲージが表示される。<br>
			そこで数字を選んでからCutすると、その枚数で裏向きに分割される</li>
		</ul>
		<ul>
			<li>上記を駆使して、進歩と職業10枚ずつ、合わせて20枚の裏向きのデッキを作る</li>
			<li>デッキを右クリック＞Search で、そのデッキの中身が右側に表示される。</li>
			<li>欲しいものをドラッグして机に出す。裏向きで机に置かれる。<br>
			それを右クリックしてDeal＞自分の色を選択すると手札に入る。</li>
			<li>残りを次の人に回す。次の人はまたデッキ＞Search で選んでいく</li>
			<li>手札のカードを確認するには[Alt]を押す</li>
		</ul>
	</p>
	<h3>ゲーム進行</h3>
		<ul>
			<li>START GAMEでラウンド1が始まる。追加アクションボードは自動で置かれないので、手動で置く。</li>
			<li>右上のボタンを押すと、その資材が1個自分の農場に降ってくる。</li>
			<li>ドラッグで支払う資材をまとめて選択して「選択中の資材を廃棄」でまとめて支払える</li>
			<li>資材をまとめたい場合には、ドラッグで範囲選択して「G」</li>
			<li>部屋・畑タイルなど裏返したいときには「F」</li>
			<li>誰のターンなのか分かりづらくなりがちなので、自分の行動は声に出して共有する「じゃ、木3」「大進歩入ってかまど取ります」など</li>
			<li>NEXT ROUNDで次のラウンドに移行する（累積の物は自動で置いてくれる。家族は帰ってくる）</li>
			<li>END GAMEで終了。誰かがこのサイトに戦績を作成して、各々が得点データを入力する。順位は適当でいい</li>
			<li>まずは得点だけ先に登録を確定してください。余裕あれば、再編集してスクショと出した職業・進歩も入れてね。</li>
			<li>全員が入力後、戦績を作成した人が、下のほうのメニューから順位の再計算を実行する</li>
			<li>スクショはWindowsならSnipping Toolおすすめ（OS標準でインストールされてる）<br>
			スクショを取るために視点を変えるには、右クリック押しながらドラッグ</li>
			<li>職業・進歩はAltでカードを拡大すると番号を確認しやすい</li>
		</ul>

	<h3>主な操作</h3>
		<ul>
			<li>左クリックからのドラッグで1つ移動。左クリックしたまましばらく待っていると、まとめて全て移動</li>
			<li>左クリックでドラッグ状態で右クリック、もしくは左クリックのドラッグで領域選択して「G」でグループ化</li>
			<li>「W」「A」「S」「D」で視点を上下左右に移動</li>
			<li>「Z」でズーム、スペースで視点をリセット</li>
			<li>右クリックからのドラッグで視点の角度を変更</li>
			<li>「Q」「E」もしくはマウスホイールで物体の角度を変更（柵など）<br>
			右上に「15°」などと出ているところが、回転単位。45°に設定するのを推奨</li>
			<li>「Alt」でカードを拡大</li>
			<li>「F」でカードを裏返す</li>
			<li>他に必要な操作ある…？</li>
		</ul>
